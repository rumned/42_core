#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	size_t	n;

	n = 0;
	if (!s || !*s)
		return ;
	while (s[n])
	{
		ft_putchar_fd(s[n], fd);
		n++;
	}
}
