#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long	num;

	num = n;
	if (num < 0)
	{
		write(fd, "-", 1);
		num = -num;
	}
	if (num <= 9)
		write(fd, &"0123456789"[num], 1);
	else if (num >= 10)
	{
		ft_putnbr_fd(num / 10, fd);
		write(fd, &"0123456789"[num % 10], 1);
	}
}
